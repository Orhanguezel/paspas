#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const projectRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..');
const localeDir = path.join(projectRoot, 'src', 'locale');
const outFile = path.join(projectRoot, 'src', 'i18n', 'generated-locales.ts');

function toImportName(code) {
  return `locale_${code.replace(/[^a-zA-Z0-9_]/g, '_')}`;
}

function isJsonLocaleFile(file) {
  return file.toLowerCase().endsWith('.json');
}

function toLocaleCode(file) {
  return file.replace(/\.json$/i, '').trim().toLowerCase();
}

function isValidLocaleCode(code) {
  return /^[a-z]{2,3}(?:-[a-z0-9]+)*$/i.test(code);
}

if (!fs.existsSync(localeDir)) {
  throw new Error(`Locale directory not found: ${localeDir}`);
}

const localeFiles = fs
  .readdirSync(localeDir, { withFileTypes: true })
  .filter((entry) => entry.isFile() && isJsonLocaleFile(entry.name))
  .map((entry) => entry.name)
  .sort((a, b) => a.localeCompare(b));

const locales = localeFiles
  .map((file) => toLocaleCode(file))
  .filter((code) => isValidLocaleCode(code));

const imports = locales.map((code) => `import ${toImportName(code)} from '@/locale/${code}.json';`);

const mapLines = locales.map((code) => `  '${code}': ${toImportName(code)},`);

const content = `// AUTO-GENERATED FILE. DO NOT EDIT.
// Generated by scripts/generate-locales-manifest.mjs

${imports.join('\n')}

export const LOCALE_MESSAGES = {
${mapLines.join('\n')}
} as const;

export type LocaleCode = keyof typeof LOCALE_MESSAGES;
export const LOCALE_CODES = Object.keys(LOCALE_MESSAGES) as LocaleCode[];
`;

fs.writeFileSync(outFile, content, 'utf8');
console.log(`Generated ${path.relative(projectRoot, outFile)} with ${locales.length} locale(s).`);
